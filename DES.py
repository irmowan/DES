def s_box(sbox_in):
    sbox_out = []
    for i in range(8):
        box_in = sbox_in[6 * i:6 * i + 6]
        row = box_in[0] * 2 + box_in[5]
        column = box_in[1] * 8 + box_in[2] * 4 + box_in[3] * 2 + box_in[4]
        num = Sbox[i][row * 16 + column]
        bin_num = '{0:04b}'.format(num)
        box_out = [int(bin_num[x]) for x in range(4)]
        sbox_out += box_out
    return sbox_out


